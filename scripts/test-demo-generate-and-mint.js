/* eslint-disable @typescript-eslint/no-var-requires */
const assert = require('assert');

const {
  buildMetadata,
  getDemoConfig,
} = require('./demo-generate-and-mint.lib.js');

function testBuildMetadata() {
  const metadata = buildMetadata({
    prompt: 'blue fire griffin',
    imageUri: 'ipfs://image-hash',
  });

  assert.strictEqual(metadata.name, 'AI NFT - blue fire griffin');
  assert.strictEqual(metadata.image, 'ipfs://image-hash');
  assert.strictEqual(metadata.prompt, 'blue fire griffin');
  assert.deepStrictEqual(metadata.attributes, [
    {
      trait_type: 'Generated By',
      value: 'Mintly AI',
    },
    {
      trait_type: 'Prompt',
      value: 'blue fire griffin',
    },
  ]);
}

function testGetDemoConfigDefaults() {
  const config = getDemoConfig({
    NEXT_PUBLIC_NFT_CONTRACT_ADDRESS:
      '0x5FbDB2315678afecb367f032d93F642f64180aa3',
  });

  assert.strictEqual(config.appUrl, 'http://127.0.0.1:8080');
  assert.strictEqual(config.rpcUrl, 'http://127.0.0.1:8545');
  assert.strictEqual(
    config.contractAddress,
    '0x5FbDB2315678afecb367f032d93F642f64180aa3',
  );
  assert.strictEqual(
    config.privateKey,
    '0xac0974bec39a17e36ba4a6b4d238ff944bacb478cbed5efcae784d7bf4f2ff80',
  );
}

function testGetDemoConfigUsesOverrides() {
  const config = getDemoConfig({
    APP_URL: 'http://127.0.0.1:9000',
    LOCAL_RPC_URL: 'http://127.0.0.1:9545',
    LOCAL_DEMO_PRIVATE_KEY: '0xabc',
    CONTRACT_ADDRESS: '0x1111111111111111111111111111111111111111',
  });

  assert.strictEqual(config.appUrl, 'http://127.0.0.1:9000');
  assert.strictEqual(config.rpcUrl, 'http://127.0.0.1:9545');
  assert.strictEqual(config.privateKey, '0xabc');
  assert.strictEqual(
    config.contractAddress,
    '0x1111111111111111111111111111111111111111',
  );
}

function main() {
  testBuildMetadata();
  testGetDemoConfigDefaults();
  testGetDemoConfigUsesOverrides();
}

main();
